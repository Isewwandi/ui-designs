package com.techleadintl.android.app1;

public class Mymodal {


    Integer logo;
    String name;

    public Mymodal(){

    }

    public Mymodal(Integer logo, String name) {
        this.logo = logo;
        this.name = name;
    }

    public Integer getLogo() {
        return logo;
    }

    public String getName() {
        return name;
    }
}
